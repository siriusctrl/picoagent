use std::sync::Arc;

use picoagent::{
    agent::runner::{AgentRunner, AgentRunnerConfig, RunRequest, RunnerOptions},
    artifact::ArtifactStore,
    events::NoopEventSink,
    hooks::HookPipeline,
    model::{MessageContent, OpenAiCompatibleOptions, OpenAiCompatibleProvider, OpenAiProtocol},
    storage::RunDirStore,
    tools::ToolRegistry,
};
use serde_json::{Value, json};
use tempfile::TempDir;
use wiremock::{
    Mock, MockServer, ResponseTemplate,
    matchers::{body_partial_json, method, path},
};

#[tokio::test]
async fn chat_reasoning_is_persisted_as_a_separate_trajectory_channel() {
    let server = MockServer::start().await;
    let body = concat!(
        "data: {\"choices\":[{\"delta\":{\"reasoning_content\":\"inspect \"}}]}\n\n",
        "data: {\"choices\":[{\"delta\":{\"reasoning_content\":\"\",\"content\":\"\"}}]}\n\n",
        "data: {\"choices\":[{\"delta\":{\"reasoning_content\":\"first\"}}]}\n\n",
        "data: {\"choices\":[{\"delta\":{\"content\":\"PICO_REASONING_OK\"}}]}\n\n",
        "data: {\"choices\":[{\"delta\":{},\"finish_reason\":\"stop\"}]}\n\n",
        "data: {\"choices\":[],\"usage\":{\"prompt_tokens\":10,\"completion_tokens\":7,\"completion_tokens_details\":{\"reasoning_tokens\":5}}}\n\n",
        "data: [DONE]\n\n",
    );
    Mock::given(method("POST"))
        .and(path("/v1/chat/completions"))
        .and(body_partial_json(json!({
            "reasoning_effort": "high",
            "max_completion_tokens": 128
        })))
        .respond_with(
            ResponseTemplate::new(200)
                .insert_header("content-type", "text/event-stream")
                .set_body_string(body),
        )
        .expect(1)
        .mount(&server)
        .await;

    let workspace = TempDir::new().unwrap();
    let store = RunDirStore::new(workspace.path());
    let options = OpenAiCompatibleOptions::new(
        format!("{}/v1", server.uri()),
        "test-key",
        OpenAiProtocol::ChatCompletions,
    );
    let provider = OpenAiCompatibleProvider::with_options(options).with_reasoning_effort("high");
    let runner = AgentRunner::new(AgentRunnerConfig {
        provider: Arc::new(provider),
        model: "reasoning-model".into(),
        workspace: workspace.path().to_path_buf(),
        skill_catalog: String::new(),
        tools: ToolRegistry::default(),
        artifacts: ArtifactStore::default(),
        store: store.clone(),
        hooks: HookPipeline::new(),
        memory: None,
        extra_events: Arc::new(NoopEventSink),
        options: RunnerOptions {
            max_output_tokens: Some(128),
            ..RunnerOptions::default()
        },
    });

    let result = runner
        .run(RunRequest::root("test reasoning"))
        .await
        .unwrap();
    assert_eq!(result.final_output, "PICO_REASONING_OK");

    let messages = store.load_messages(&result.run_id).await.unwrap();
    assert!(matches!(
        &messages[0].content[0],
        MessageContent::RuntimeReminder { text }
            if text.starts_with("<runtime-reminder>\n<environment>")
                && text.ends_with("</runtime-reminder>")
                && !text.contains("generated by picoagent")
    ));
    assert!(matches!(
        &messages[0].content[1],
        MessageContent::Text { text } if text == "test reasoning"
    ));
    assert!(matches!(
        &messages[1].content[0],
        MessageContent::Reasoning { text } if text == "inspect first"
    ));
    assert!(matches!(
        &messages[1].content[1],
        MessageContent::Text { text } if text == "PICO_REASONING_OK"
    ));

    let requests = server.received_requests().await.unwrap();
    let request_body: Value = serde_json::from_slice(&requests[0].body).unwrap();
    let user_content = request_body["messages"][1]["content"].as_str().unwrap();
    assert!(user_content.starts_with("<runtime-reminder>\n<environment>"));
    assert!(user_content.ends_with("</runtime-reminder>\n\ntest reasoning"));

    let paths = store.paths(&result.run_id);
    let persisted: Vec<Value> = tokio::fs::read_to_string(&paths.messages)
        .await
        .unwrap()
        .lines()
        .map(|line| serde_json::from_str(line).unwrap())
        .collect();
    assert_eq!(persisted.len(), 2);
    assert_eq!(
        persisted[0].as_object().unwrap().keys().collect::<Vec<_>>(),
        ["content", "role"]
    );
    assert_eq!(persisted[0]["role"], "user");
    assert_eq!(persisted[0]["content"], user_content);
    assert!(!persisted[0].to_string().contains("runtime_reminder"));
    assert_eq!(
        persisted[1],
        json!({
            "role": "assistant",
            "content": "PICO_REASONING_OK",
            "reasoning_content": "inspect first"
        })
    );
    for message in &persisted {
        assert!(message.get("message_id").is_none());
        assert!(message.get("seq").is_none());
        assert!(message.get("created_at").is_none());
    }

    let metadata: Vec<Value> = tokio::fs::read_to_string(&paths.message_metadata)
        .await
        .unwrap()
        .lines()
        .map(|line| serde_json::from_str(line).unwrap())
        .collect();
    assert_eq!(metadata.len(), 2);
    assert_eq!(metadata[0]["seq"], 1);
    assert_eq!(metadata[1]["seq"], 2);
    assert!(metadata.iter().all(|entry| entry["message_id"].is_string()));

    let events = tokio::fs::read_to_string(paths.events).await.unwrap();
    let events: Vec<Value> = events
        .lines()
        .map(|line| serde_json::from_str(line).unwrap())
        .collect();
    assert_eq!(
        events
            .iter()
            .map(|event| event["type"].as_str().unwrap())
            .collect::<Vec<_>>(),
        [
            "run_started",
            "model_started",
            "model_completed",
            "run_completed"
        ]
    );
    let completed = events
        .iter()
        .find(|event| event["type"] == "model_completed")
        .unwrap();
    assert_eq!(completed["reasoning_tokens"], 5);
    assert_eq!(
        tokio::fs::read_to_string(paths.final_output).await.unwrap(),
        "PICO_REASONING_OK"
    );
}
